            ////////////////////////////////////////////////////////////////////
            bool createCommandBuffers();

            ////////////////////////////////////////////////////////////////////
            //  Create semaphores                                             //
            //  return : True if semaphores are successfully created          //
            ////////////////////////////////////////////////////////////////////
